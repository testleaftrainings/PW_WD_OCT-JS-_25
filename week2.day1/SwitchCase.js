let browser="chrome"
//work based on Key and value
//key -> variable name
//value -> value

switch (browser) {
    case "chrome":
        console.log("Chrome Browser is open")
        break
    case "edge":
        console.log("edge browser is open")
        break
    case "webkit":
        console.log("webkit browser is open")
        break

    default:
        console.log("No Browser")
        break;
}